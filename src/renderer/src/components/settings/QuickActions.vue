const allTools: Tool[] = [
  { id: 'show-desktop', name: '返回桌面', icon: icons.desktop },
  { id: 'task-view', name: '多任务视角', icon: icons.taskView },
  { id: 'taskmgr', name: '任务管理器', icon: icons.taskManager },
  { id: 'terminal', name: '终端', icon: icons.terminal },
  { id: 'gpedit', name: '组策略', icon: icons.gpedit, requiresCheck: true },
  { id: 'regedit', name: '注册表', icon: icons.regedit },
  { id: 'services', name: '服务', icon: icons.services },
  { id: 'services', name: '服务', icon: icons.services },
  { id: 'devmgmt', name: '设备管理器', icon: icons.devmgmt },
  { id: 'logs', name: '日志目录', icon: icons.folder },
]

const availableTools = computed(() => {
  return allTools.filter(tool => {
    if (tool.requiresCheck) {
      return capabilities.value[tool.id] !== false
    }
    return true
  })
})

const executeTool = (toolId: string) => {
  if (toolId === 'logs') {
    if (window.electronAPI && window.electronAPI.openLogDirectory) {
      window.electronAPI.openLogDirectory();
    }
    return;
  }

  if (window.electronAPI && window.electronAPI.executeSystemTool) {
    window.electronAPI.executeSystemTool(toolId)
  } else {
    console.error('electronAPI.executeSystemTool is not available')
  }
}

onMounted(async () => {
  // Check capabilities for tools that require it
  for (const tool of allTools) {
    if (tool.requiresCheck) {
      try {
        if (window.electronAPI && window.electronAPI.checkSystemCapability) {
          const isAvailable = await window.electronAPI.checkSystemCapability(tool.id)
          capabilities.value[tool.id] = isAvailable
        }
      } catch (e) {
        console.error(`Failed to check capability for ${tool.id}`, e)
        capabilities.value[tool.id] = false
      }
    }
  }
})
